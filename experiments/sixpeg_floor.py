"""Six-Peg Collapse: the middle capacity point (18 cells) between 9 and 27.

Six-Peg Collapse is a **sibling** of Collapse3 in the same family as Three-Peg
Collapse (see ``experiments/threepeg_floor.py``): placements are restricted to
the top two rows of pegs (0, 1, 2, 3, 4, 5) and *every other mechanic is
untouched* -- stacking, gravity, the Collapse/removal with all five legality
conditions, cooldown, the Oops rule, line wins, attrition, and the repo rule
(no legal action -> immediate attrition end). It is a pure move-legality
restriction, not a forked engine; pegs 6-8 stay empty, so only 16 of the full
board's 49 winning lines can ever fire (6 verticals, 2 rows at 3 levels, 4
staircases).

Why it exists. Finding 12(iv) names this experiment explicitly: *if* board
capacity sets the phase boundary the three-peg sibling exposed near (6,6)-(7,7),
then a larger board should push that boundary later. Six pegs give an 18-cell
board -- the middle data point between the 9-cell single row (Three-Peg) and the
27-cell full board (Collapse3). Three capacity points test whether the boundary
location scales with capacity.

SCOPE -- read this first. This is PARALLEL evidence about the same mechanics
under a THIRD geometry. Its numbers are NOT facts about Collapse3 and must never
be quoted as such. The value is the contrast across three boards, one engine.

Feasibility. Unlike the single row, the two-row board is NOT capped small: the
un-folded reachable set explodes ~3-4x per reserve (9,152,201 states already at
(7,7)), so enumeration is memory-bound around (7,7)-(8,8) and the sibling's own
boundary (predicted ~(12,12)+) is out of reach. What the feasible (2..7) range
DOES establish is the *direction*: every boundary signature the three-peg
sibling showed near (6,6)-(7,7) is delayed by ~2 reserve sizes here (see
Finding 13), which is the scaling 12(iv) predicted.

Run:  python -m experiments.sixpeg_floor            # default 2..6 (safe)
      python -m experiments.sixpeg_floor 2 3 4 5 6 7  # include (7,7) (~20 min, heavy RAM)
"""

import sys
import time

from collapse3.aliasing import OBSERVATIONS, regret_floor
from collapse3.enumeration import solve_all
from collapse3.game import apply_move, empty_state, placement_pegs
from experiments._provenance import announce, write_result
from experiments.threepeg_floor import winning_line_census

NAME = "sixpeg_floor"
PLACEMENT_ROWS = (0, 1, 2, 3, 4, 5)

_LABEL = {100: "P0 line", 10: "P0 attrition", 0: "draw", -10: "P1 attrition", -100: "P1 line"}

# --- Reproduce gate (deterministic; ABORT on any mismatch = engine drift) ---
# These numbers were generated by this experiment and then frozen; a mismatch on
# re-run means the engine changed under the sibling. res -> (states, decisions,
# hide_cooldown floor, hide_reserves floor).
EXPECTED_FLOORS = {
    2: (1126, 253, 0.0000, 0.0000),
    3: (16759, 6100, 0.0010, 0.0049),
    4: (142173, 66643, 0.0071, 0.0618),
    5: (799389, 430543, 0.0112, 0.1240),
    6: (3167065, 1855107, 0.0142, 0.1790),
    7: (9152201, 5627479, 0.0167, 0.2319),
}
# res -> (root value, [value after place on pegs 0..5], P0-perspective)
EXPECTED_OPENINGS = {
    2: (0, [0, 0, 0, 0, 0, 0]),
    3: (0, [0, 0, 0, 0, 0, 0]),
    4: (0, [0, 0, 0, 0, 0, 0]),
    5: (0, [0, 0, 0, 0, 0, 0]),
    6: (10, [0, 10, 0, 0, 10, 0]),   # root turns: centres win, ends draw
    7: (10, [0, 10, 0, 0, 10, 0]),   # still "centre uniquely wins" (three-peg was here at (5,5))
}
_FLOOR_TOL = 5e-5


def analyse_size(r):
    """Solve (r,r) once under the six-peg restriction; return openings + floors.

    One ``solve_all`` per size feeds both the opening values and all three
    aliasing floors, so the expensive enumeration is not repeated.
    """
    memo = solve_all(empty_state(r, r))
    root = empty_state(r, r)
    root_v = memo[root]
    place_v = [memo[apply_move(root, ("place", peg))] for peg in PLACEMENT_ROWS]

    rows = {}
    decisions = 0
    for name in ("hide_cooldown", "hide_reserves", "hide_both"):
        res = regret_floor(memo, OBSERVATIONS[name])
        decisions = res.n_decisions
        rows[name] = {
            "floor": res.floor,
            "obs_groups": res.obs_groups,
            "aliased_groups": res.aliased_groups,
            "conflict_groups": res.conflict_groups,
            "no_common_legal_action": res.no_common_legal_action,
        }
    return {
        "states": len(memo),
        "decisions": decisions,
        "rows": rows,
        "root": root_v,
        "placements": place_v,
    }


def main(sizes=tuple(range(2, 7))):
    census = winning_line_census(PLACEMENT_ROWS)
    assert census == {"total": 49, "live": 16, "verticals": 6, "row_levels": 6,
                      "staircases": 4}, census

    with placement_pegs(PLACEMENT_ROWS):
        print(f"Six-Peg Collapse -- sibling variant (placements on pegs {list(PLACEMENT_ROWS)})")
        print(f"Live winning lines: {census['live']} of {census['total']} "
              f"({census['verticals']} verticals, {census['row_levels']} row-levels, "
              f"{census['staircases']} staircases)\n")

        print("Aliasing-floor curve + openings (uniform over decision states, WDL units):")
        print(f"  {'res':>7} {'states':>10} {'decisions':>11} {'hide_cd':>9} "
              f"{'d(cd)':>8} {'hide_res':>9} {'d(res)':>8} {'hide_both':>10} "
              f"{'charity':>9} {'time':>8}")
        by_size = {}
        openings = {}
        prev_cd = prev_res = None
        exactness_holds = True
        for r in sizes:
            t0 = time.time()
            info = analyse_size(r)
            dt = time.time() - t0
            rows = info["rows"]
            cd = rows["hide_cooldown"]["floor"]
            rv = rows["hide_reserves"]["floor"]
            hb = rows["hide_both"]["floor"]
            cd_no = rows["hide_cooldown"]["no_common_legal_action"]
            rv_no = rows["hide_reserves"]["no_common_legal_action"]
            if cd_no or rv_no:
                exactness_holds = False
            d_cd = None if prev_cd is None else round(cd - prev_cd, 4)
            d_rv = None if prev_res is None else round(rv - prev_res, 4)

            exp = EXPECTED_FLOORS.get(r)
            if exp is not None:
                assert info["states"] == exp[0], f"state drift at ({r},{r}): {info['states']} != {exp[0]}"
                assert info["decisions"] == exp[1], f"decision drift at ({r},{r}): {info['decisions']} != {exp[1]}"
                assert abs(cd - exp[2]) < _FLOOR_TOL, f"hide_cooldown drift at ({r},{r}): {cd} != {exp[2]}"
                assert abs(rv - exp[3]) < _FLOOR_TOL, f"hide_reserves drift at ({r},{r}): {rv} != {exp[3]}"
            exp_o = EXPECTED_OPENINGS.get(r)
            if exp_o is not None:
                assert info["root"] == exp_o[0] and info["placements"] == exp_o[1], (
                    f"OPENING GATE MISMATCH at ({r},{r}): got root={info['root']} "
                    f"places={info['placements']}, expected {exp_o} -- ENGINE DRIFT, aborting.")

            openings[f"{r}_{r}"] = {
                "root": info["root"], "root_label": _LABEL[info["root"]],
                "placements": [{"peg": p, "value": v, "label": _LABEL[v]}
                               for p, v in zip(PLACEMENT_ROWS, info["placements"])],
            }
            by_size[f"{r}_{r}"] = {
                "states": info["states"],
                "decisions": info["decisions"],
                "rows": rows,
                "hide_cooldown_increment": d_cd,
                "hide_reserves_increment": d_rv,
                "seconds": round(dt, 2),
            }
            print(f"  ({r:>2},{r:<2}) {info['states']:>10,} {info['decisions']:>11,} "
                  f"{cd:>9.4f} {('' if d_cd is None else f'{d_cd:+.4f}'):>8} "
                  f"{rv:>9.4f} {('' if d_rv is None else f'{d_rv:+.4f}'):>8} "
                  f"{hb:>10.4f} {f'({cd_no},{rv_no})':>9} {dt:>7.1f}s", flush=True)
            prev_cd, prev_res = cd, rv

        print(f"\nExactness lemma (single-hide charity never fires) holds so far: {exactness_holds}")
        print("Opening phase (root / centre pegs 1,4 / end pegs 0,2,3,5):")
        for r in sizes:
            o = openings[f"{r}_{r}"]["placements"]
            print(f"  ({r},{r}): root {openings[f'{r}_{r}']['root_label']:>12} | "
                  f"centre {o[1]['label']:>12} | ends {o[0]['label']:>12}")

    payload = {
        "variant": "Six-Peg Collapse",
        "placement_pegs": list(PLACEMENT_ROWS),
        "winning_line_census": census,
        "sizes": list(sizes),
        "openings": openings,
        "by_size": by_size,
        "exactness_lemma_holds": exactness_holds,
    }
    path = write_result(NAME, {"sizes": list(sizes), "placement_pegs": list(PLACEMENT_ROWS)}, payload)
    announce(NAME, path)


if __name__ == "__main__":
    # Default stops at (6,6): un-folded enumeration explodes ~3-4x per reserve
    # (9.15M states at (7,7)), so (7,7)+ is time/memory heavy -- pass sizes
    # explicitly to probe higher.
    args = [int(a) for a in sys.argv[1:]]
    main(tuple(args) if args else tuple(range(2, 7)))
